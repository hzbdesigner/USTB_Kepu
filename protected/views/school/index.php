						<p>
							<span class="dropcap color">学</span>校位于高校云集的北京市海淀区学院路，占地约80.46万平方米（包括管庄校区），校舍建筑总面积84.49万平方米（包括管庄校区）。学校现有1个国家科学中心，2个国家重点（专业）实验室，2个国家工程（技术）研究中心，2个国家科技基础条件平台，21个省、部级重点实验室、工程研究中心。特别是2007年，学校作为唯一一所教育部直属高校牵头承担了国家重大科技基础设施项目——重大工程材料服役安全研究评价设施，并负责筹建国家材料服役安全科学中心。图书馆藏书172万册。定期出版《北京科技大学学报》、《北京科技大学学报（社会科学版）》《International Journal of Minerals, Metallurgy and Materials》、《Rare Metals》、《思想教育研究》、《物流技术与应用》、《金属世界》、《粉末冶金技术》等重要学术刊物。
						</p>
						<p>
							学校由土木与环境工程学院、冶金与生态工程学院、材料科学与工程学院、机械工程学院、自动化学院、计算机与通信工程学院、数理学院、化学与生物工程学院、东凌经济管理学院、文法学院、外国语学院、马克思主义学院、高等工程师学院，远程与成人教育学院、继续教育学院、国际学院，以及研究生院、体育部、管庄校区、天津学院、延庆分校组成。现有18个一级学科博士授权点，73个博士学科点，118个硕士学科点，另有MBA（含EMBA）、MPA、法律硕士、会计硕士、翻译硕士和20个领域的工程硕士专业学位授予权，11个博士后科研流动站，46个本科专业。学校冶金、材料、矿业、科技史4个全国一级重点学科学术水平蜚声中外（据教育部一级学科评估结果，冶金、科技史排名全国第1，材料第2，矿业第3）；机械、热能等学科享有盛誉；控制、力学、计算机、管理、思想政治教育等一批学科具有雄厚实力；一批新兴学科，如通信工程、电子信息、土木工程、环境工程等焕发出勃勃生机。
						</p>
						<p>
							学校拥有一支治学严谨的师资队伍。教职工总数2917人，其中专任教师1789人。专任教师中有中国科学院院士5人，中国工程院院士2人（双聘1人），国务院学位委员会委员1人，国家学科评议组成员5人，国家“973”首席专家3人，国家“千人计划”入选者6人，国家级有突出贡献专家14人，部级有突出贡献专家13人，“长江学者奖励计划”特聘教授13人、讲座教授3人，国家杰出青年科学基金获得者14人，国家“百千万人才工程”入选12人，教育部新世纪优秀人才支持计划75人，具有正高级专业技术职务的教职工438人，具有副高级专业技术职务的教职工753人。人才梯队正在壮大，一大批中青年学者脱颖而出。
